function generateTasks(planId: string, taskDefs: Omit<StudyPlanTask, 'id' | 'completed' | 'completedDate'>[]): StudyPlanTask[] {
  return taskDefs.map((t, i) => ({
    ...t,
    id: `${planId}_task_${i + 1}`,
    completed: false,
  }));
}

